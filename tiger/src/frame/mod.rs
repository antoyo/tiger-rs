use temp::Label;

pub mod x86_64;

pub trait Frame {
    type Access: Clone;

    fn new(name: Label, formals: Vec<bool>) -> Self;

    fn name(&self) -> Label;

    fn formals(&self) -> &[Self::Access];

    fn alloc_local(&mut self, escape: bool) -> Self::Access;
}
